auto operator<<(
    ::std::ostream& os,
    const ::xrn::ecs::Signature& signature
) -> ::std::ostream&
{
    for (auto i{ 0uz }; i < ::xrn::ecs::component::maxId; ++i) {
        os << signature[i];
    }
    return os;
}
