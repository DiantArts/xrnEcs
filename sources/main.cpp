auto main()
    -> int
{
    ::xrn::ecs::Registry<int, float> registry;
    auto entity{ registry.createEntity(1, 5.f) };
    registry.add<float>(entity, 5);
    ::fmt::print("{}\n", registry.has<int>(entity));
    ::fmt::print("{}\n", registry.has<float>(entity));
    ::fmt::print("{}\n", registry.get<float>(entity).get());
    return EXIT_SUCCESS;
}
